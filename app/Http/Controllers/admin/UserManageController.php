<?php

namespace App\Http\Controllers\admin;

use App\Http\Controllers\Controller;
use Illuminate\Http\Request;
use App\Models\User;

class UserManageController extends Controller
{
    /**
     * Display a listing of the users.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        return view('pages.admin.users.index', [
            'users' => User::where('is_admin', '=', 0)->orderBy('id', 'DESC')->paginate(10), 
            'i' => (request()->input('page', 1) - 1) * 10
        ]);
    }

    /**
     * Show the form for creating a new user.
     *
     * @return \Illuminate\Http\Response
     */
    public function create()
    {
        return view('pages.admin.users.create');
    }

    /**
     * Store a newly created user.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request->validate([
            'name' => 'required',
            'phone' => 'required',
        ]);
        User::create($request->all());
   
        return redirect()->route('admin.users.index')
                        ->with('success','User created successfully.');
    }

    /**
     * Display the specified user information.
     *
     * @param  \App\User  $user
     * @return \Illuminate\Http\Response
     */
    public function show(User $user)
    {
        return view('pages.admin.users.show',compact('user'));
    }

    /**
     * Show the form for editing the specified user information.
     *
     * @param  \App\User  $user
     * @return \Illuminate\Http\Response
     */
    public function edit(User $user)
    {
        return view('pages.admin.users.edit',compact('user'));
    }

    /**
     * Update the specified user information.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  \App\Modal\User  $user
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, User $user)
    {
        $request->validate([
            'name' => 'required|max:120',
            'phone' => 'required|min:10|numeric',
        ]);
  
        $user->update($request->all());
  
        return redirect()->route('admin.users.index')
                        ->with('success','User updated successfully');
    }

    /**
     * Remove the specified user.
     *
     * @param  \App\Models\User  $user
     * @return \Illuminate\Http\Response
     */
    public function destroy(User $user)
    {
        $user->delete();
  
        return redirect()->route('admin.users.index')
                        ->with('success','User deleted successfully');
    }
}
